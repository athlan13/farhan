    private void KoneksiDatabase() {
        int row = table.getRowCount();
    for (int a = 0; a < row; a++) {
        model.removeRow(0);
    }
    try {
        Connection cn = DriverManager.getConnection("jdbc:mysql://localhost:3307/db_frmpenjualanbarang", "root", "");
        ResultSet rs = cn.createStatement().executeQuery("SELECT * FROM tb_frmpenjualanbarang");
        while (rs.next()) {
            String data[] = {
                rs.getString("nama_barang"),
                rs.getString("barang"),
                "Rp. " + rs.getString("harga"),
                rs.getString("jumlah_barang")
            };
            model.addRow(data);
        }
    } catch (SQLException ex) {
        Logger.getLogger(FrmPenjualanBarang.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(null, "Gagal terhubung ke database!");
    }
    }
    
    
