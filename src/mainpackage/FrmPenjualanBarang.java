    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
    String Namabarang = txtNamabarang.getText();
    String Barang = txtbarang.getText();
    int Harga = Integer.parseInt(txtHarga.getText());
    int Jumlahbarang = Integer.parseInt(txtJumlahbarang.getText());
    int SubTotal = Harga * Jumlahbarang;

    DefaultTableModel tbl = (DefaultTableModel) table.getModel();
        
    tbl.addRow(new Object[]{
        Namabarang,
        Barang,
        "Rp. "+Harga, 
        Jumlahbarang,
        "Rp. "+SubTotal   
        });
        
        int TotalHarga =0;
        for(int i=0; i<table.getRowCount();i++){
            TotalHarga += Integer.parseInt(table.getValueAt(i, 4).toString().substring(4));
        }
        txtTotalHarga.setText("Rp. "+TotalHarga); 
        txtNamabarang.setText("");
        txtbarang.setText("");
        txtHarga.setText("");
        txtJumlahbarang.setText("");
        
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost:3307/db_frmpenjualanbarang", "root", "");
            cn.createStatement().executeUpdate("INSERT INTO tb_frmpenjualanbarang (nama_barang, barang, harga, jumlah_barang) VALUES ('" + Namabarang + "', '" + Barang + "', '" + Harga + "', '" + Jumlahbarang + "')");
            KoneksiDatabase();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "ada yang belom diisi !!");
        }
        
    }                                        

